import java.util.Scanner;

public class programtwenty
{




    public static void main(String[] args) {
        int x,y,z;
        for(x=1;x<=3;x++)
        {
            for(y=2;y<=3;y++)
            {
                z=x*x*x+y*y*y;
                System.out.println(x+ " " + y + " " +z);
            }
        }
    }
}

