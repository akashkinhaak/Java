import java.util.Scanner;

public class programfourteen
{
    public static void main(String[] args)
    {
        int num = 1, s =0;
        while (num<=10)
        {
            s+=num;
            ++num;

        }
        System.out.println("Sum Of First 10 Natural Numbers is : "+s);
    }
}
